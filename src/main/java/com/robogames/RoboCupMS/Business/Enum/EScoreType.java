package com.robogames.RoboCupMS.Business.Enum;

/**
 * Score type enumeration - represents different types of scoring in matches
 */
public enum EScoreType {

    /**
     * Time-based scoring (e.g., line follower - lower is better)
     */
    TIME,

    /**
     * Point-based scoring (e.g., sumo wins - higher is better)
     */
    SCORE

}
