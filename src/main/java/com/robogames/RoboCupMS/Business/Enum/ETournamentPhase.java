package com.robogames.RoboCupMS.Business.Enum;

/**
 * Tournament phase enumeration - represents different stages of a tournament
 */
public enum ETournamentPhase {

    /**
     * Group stage - round-robin matches within groups
     */
    GROUP_STAGE,

    /**
     * Preliminary round - early bracket rounds before round of 16
     */
    PRELIMINARY,

    /**
     * Round of 16 - 8 matches
     */
    ROUND_OF_16,

    /**
     * Quarterfinal round
     */
    QUARTERFINAL,

    /**
     * Semifinal round
     */
    SEMIFINAL,

    /**
     * Final round - championship match
     */
    FINAL

}
